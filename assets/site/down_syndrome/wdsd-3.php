                            World Down Syndrome Day (WDSD) is observed on 21 March. On this day, people with Down syndrome and those who live
                            and work with them throughout the world organise and participate in activities and events to raise public awareness
                            and create  a single global voice for advocating for the rights, inclusion and well being of people with Down syndrome.<br><br>


                            The date for WDSD being the 21st day of the 3rd month, was selected to signify the uniqueness of the triplication (trisomy)
                            of the 21st chromosome which causes Down syndrome. <br><br>

                            WDSD was first observed in 2006 in many countries around the world and Down Syndrome Association Singapore launched and hosted the
                            WDSD website from 2006-2010, on behalf of Down Syndrome International, for global activities to be recorded. <br><br>

                            On 19 December 2011, the United Nations General Assembly declared 21 March as World Down Syndrome Day. The General Assembly
                            decided to "designate 21 March as World Down Syndrome Day, to be observed every year beginning in 2012;" and "invites all
                            Member States, relevant organizations of the United Nations system and other international organizations, as well as civil
                            society, including non-governmental organizations and the private sector, to observe World Down Syndrome Day in an appropriate
                            manner, in order to raise public awareness of Down syndrome;". <br><br>


                            Today, DSI co-ordinates the WDSD website (as a single global meeting place to share WDSD World Events),
                            the WDSD Global Video Event "Let Us In!", the  WDSD Conference held at the United Nations Headquarters in New York,
                            USA on 21 March, the LOTS OF SOCKS campaign and the WDSD Awards.
